package by.hrodna.shaplyka.notepad;



/**
 * 
 * @author Shaplyka
 * 
 * Note class for the Notepad
 * 
 *
 */

public class Note {
	
	private String dataNote;
	private String textNote;
	/**
	 * @return the dataNote
	 */
	public String getDataNote() {
		return dataNote;
	}
	/**
	 * @param dataNote the dataNote to set
	 */
	public void setDataNote(String dataNote) {
		this.dataNote = dataNote;
	}
	/**
	 * @return the textNote
	 */
	public String getTextNote() {
		return textNote;
	}
	/**
	 * @param textNote the textNote to set
	 */
	public void setTextNote(String textNote) {
		this.textNote = textNote;
	}
	
	
	
	

}
